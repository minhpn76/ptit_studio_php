        onLeave: function (origin, destination, direction) {
            if (destination.index == 0) {
                document.getElementById("menu").classList.remove("menu-black");
                document.getElementById("fp-nav").classList.remove("fp-nav-black");
            } else {
                document.getElementById("menu").classList.add("menu-black")
                document.getElementById("fp-nav").classList.add("fp-nav-black")
            }

            if(direction == 'up') {
                $('#fullpage').addClass('upSlide');
                $('#fullpage').removeClass('downSlide');
            } else {
                $('#fullpage').addClass('downSlide');
                $('#fullpage').removeClass('upSlide');
            }

        }
    });
